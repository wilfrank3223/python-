# Crear un diccionario llamado 'dicCuadrado' utilizando un bucle 'for' y una expresión de comprensión de diccionario.
# Las claves del diccionario son números del conjunto (2, 4, 6), y los valores son el cubo de cada número.
dicCuadrado = {x: x**3 for x in (2, 4, 6)}

# Imprimir el diccionario 'dicCuadrado'.
print(dicCuadrado)
