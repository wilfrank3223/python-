# Definir una función llamada 'dias' que toma dos parámetros: arg y resultado (con una lista vacía como valor predeterminado).
def dias(arg, resultado=[]):
    # Agregar el valor de 'arg' a la lista 'resultado'.
    resultado.append(arg)
    # Imprimir la lista 'resultado'.
    print(resultado)

# Llamar a la función 'dias' con el argumento 'Domingo' y una lista vacía como 'resultado'.
